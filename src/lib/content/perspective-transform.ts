/**
 * 全ての視点変換タイプのリストを返す
 */
export function getAllPerspectiveTypes(): PerspectiveType[] {
  return [
    'experience_to_lesson',
    'personal_to_universal',
    'concept_to_practice',
    'case_to_work',
    'past_to_modern',
    'deep_to_intro',
  ];
}

/**
 * 視点変換タイプに応じたプロンプト指示文を返す
 * @param type 視点変換タイプ
 * @returns プロンプト指示文
 */
export function getPerspectivePrompt(type: PerspectiveType): string {
  const prompt = PERSPECTIVE_PROMPTS[type];
  if (!prompt) {
    throw new Error(`Unknown perspective type: ${type}`);
  }
  return prompt;
}

/**
 * 視点変換タイプが有効かどうかを判定する
 * @param type チェック対象の文字列
 * @returns PerspectiveType であれば true
 */
export function isValidPerspectiveType(type: string): type is PerspectiveType {
  return type in PERSPECTIVE_PROMPTS;
}
