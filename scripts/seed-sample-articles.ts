async function main() {
  console.log('Inserting 3 sample published articles...\n');

  for (const article of articles) {
    const { data, error } = await supabase
      .from('articles')
      .insert(article)
      .select('id, title, slug, status')
      .single();

    if (error) {
      console.error(`  FAIL: ${article.title}`);
      console.error(`        ${error.message}`);
    } else {
      console.log(`  OK: ${data.title}`);
      console.log(`      /column/${data.slug}`);
    }
  }

  console.log('\nDone!');
}

main().catch((err) => {
  console.error('Unexpected error:', err);
  process.exit(1);
});
